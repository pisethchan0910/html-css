var currentPage = 1;
var rowsPerPage = 10;
const createTable = () => {
  const start = (currentPage - 1) * rowsPerPage;
  const end = start + parseInt(rowsPerPage);
  const paginatedData = data.slice(start, end);

  const table = document.getElementById("myTable");
  table.innerHTML = `
      <thead>
          <tr>
              <th><input type="checkbox" class="row-checkbox" id="checkAll" onclick="checkAllBoxes()"></th>
              <th>NO</th>
              <th>기존유형</th>
              <th>신청유형</th>
              <th>제출서류</th>
              <th>신청일시</th>
              <th>승인여부</th>
              <th>승인거부 사유</th>
              <th>승인일시</th>
          </tr>
      </thead>
      <tbody>
      </tbody>
    `;

  const tbody = table.querySelector("tbody");

  paginatedData.forEach((item, i) => {
    const rowNumber = start + i + 1;
    const color =
      rowNumber % 3 === 1
        ? "pending"
        : rowNumber % 3 === 2
        ? "approved"
        : "rejected";
    const disabled = rowNumber % 3 !== 1 ? "disabled" : "";
    const row = `
    <tr>
      <td data-label=""><input type="checkbox" class="row-checkbox child-checkbox" ${disabled}></td>
      <td data-label="NO">${rowNumber}</td>
      <td data-label="기존유형">${item.col2}</td>
      <td data-label="신청유형">${item.col3}</td>
      <td data-label="제출서류"><div class="disabled-box">${item.col4}</div></td>
      <td data-label="신청일시">${item.col5}</td>
      <td data-label="승인여부"><div class="status-box ${color}">${item.col6}</div></td>
      <td data-label="승인거부 사유">${item.col7}</td>
      <td data-label="승인일시">${item.col8}</td>
    </tr>
      `;
    tbody.innerHTML += row;
  });

  createPagination();
};

function createPaginationIconButton(iconName, onClickHandler) {
  const button = document.createElement("a");
  button.href = "#";
  button.onclick = onClickHandler;

  const icon = document.createElement("img");
  icon.src = `assets/button/${iconName}.svg`; // Set the source to the SVG file
  button.appendChild(icon);
  return button;
}

function createPaginationButton(innerHtml, onClickHandler) {
  const button = document.createElement("a");
  button.href = "#";
  button.innerHTML = innerHtml;
  button.onclick = onClickHandler;
  return button;
}

function createPagination() {
  const numPages = Math.ceil(data.length / rowsPerPage);

  const pagination = document.getElementById("pagination");
  pagination.innerHTML = "";

  // Create a new container for the skip buttons
  const skipButtonsContainer = document.createElement("div");
  skipButtonsContainer.className = "skip-buttons";

  const firstButton = createPaginationIconButton("first", () => {
    currentPage = 1;
    createTable();
  });
  skipButtonsContainer.appendChild(firstButton);

  const prevButton = createPaginationIconButton("previous", () => {
    currentPage = Math.max(1, currentPage - 1);
    createTable();
  });
  skipButtonsContainer.appendChild(prevButton);

  // Append the skip buttons container to the pagination
  pagination.appendChild(skipButtonsContainer);

  for (let i = 1; i <= numPages; i++) {
    const button = createPaginationButton(i, function () {
      currentPage = parseInt(this.innerHTML);
      createTable();
    });
    if (i == currentPage) {
      button.className = "active";
    }
    pagination.appendChild(button);
  }

  // Create another skip buttons container for the next and last buttons
  const skipButtonsContainer2 = document.createElement("div");
  skipButtonsContainer2.className = "skip-buttons";

  const nextButton = createPaginationIconButton("next", () => {
    currentPage = Math.min(numPages, currentPage + 1);
    createTable();
  });
  skipButtonsContainer2.appendChild(nextButton);

  const lastButton = createPaginationIconButton("last", () => {
    currentPage = numPages;
    createTable();
  });
  skipButtonsContainer2.appendChild(lastButton);

  // Append the second skip buttons container to the pagination
  pagination.appendChild(skipButtonsContainer2);
}

const checkAllBoxes = () => {
  const checkboxes = document.getElementsByClassName(
    "row-checkbox child-checkbox"
  );
  const checkAllBox = document.getElementById("checkAll");
  Array.from(checkboxes).forEach((checkbox) => {
    checkbox.checked = checkAllBox.checked;
    checkbox.disabled = checkAllBox.checked;
  });
};

